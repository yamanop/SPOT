plt.ylabel("RSS (KB)")
plt.title("Memory Spike Behaviour")
plt.savefig("plots/memory_spike.png", dpi=300)
plt.show()

plt.figure()
plt.plot(mem_leak["wtime"], mem_leak["rss"])
plt.xlabel("Time (seconds)")
plt.ylabel("RSS (KB)")
plt.title("Memory Leak Behaviour")
plt.savefig("plots/memory_leak.png", dpi=300)
plt.show()